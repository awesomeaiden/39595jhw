public class Action {
    private String msg;
    private int ival;
    private char cval;

    public Action() {

    }

    public void setMessage(String msg) {
        this.msg = msg;
    }

    public void setIntValue(int ival) {
        this.ival = ival;
    }

    public void setCharValue(char cval) {
        this.cval = cval;
    }
}
